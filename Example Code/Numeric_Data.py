# Introduction to Integer and Float Numeric Data

num = 3.14
print(type(num)) # Type Float, Int

print(3+2) # Addition
print(3-2) # Subtraction
print(3*2) # Multiplication
print(3/2) # Division
print(3**2) # Exponent
print(3%2) # Modulus
print(abs(-3)) # Absolute Value
print(3 * (2 + 1)) # Order of operations

