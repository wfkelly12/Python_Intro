# Introduction to Integer and Float Numeric Data

# Type of Numeric (Float, Int)
num = 3.14
print(type(num))

# Types of Operations
print(3+2) # Addition
print(3-2) # Subtraction
print(3*2) # Multiplication
print(3/2) # Division
print(3**2) # Exponent
print(3%2) # Modulus
print(abs(-3)) # Absolute Value
print(3 * (2 + 1)) # Order of operations

# Increment Numeric
num = 1
num += 1
print(num)

# Round Function
print(round(3.75,1))

# Comparison
num_1 = 3
num_2 = 2
print(num_1 == 3)

# Type Cast
num_1 = '100'
num_2 = '200'
print(num_1 + num_2)
num_1 = int(num_1)
num_2 = int(num_2)
print(num_1 + num_2)
